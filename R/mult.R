#' Multiplicación entre dos números
#'
#' Esta función devuelve la multiplicación entre dos números.
#'
#' @param a Primer número
#' @param b Segundo número
#'
#' @return La multiplicación entre a y b
#' @export
#'
#' @examples
#' mult(2, 3.5)
#'
#' @seealso \link{div}
mult <- function(a, b) {
  return(a * b)
}
